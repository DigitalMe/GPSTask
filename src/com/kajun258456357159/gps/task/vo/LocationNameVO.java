package com.kajun258456357159.gps.task.vo;

public class LocationNameVO {

	private long _id = -1;
	private String _name = "";
	private boolean _isBoycotted = Boolean.FALSE;
	private boolean _isDeleted = Boolean.FALSE;
	private boolean _isPrivate = Boolean.FALSE;
	private boolean _isUploaded = Boolean.FALSE;
	
	public LocationNameVO(long id, String name, boolean isBoycotted, boolean isDeleted, boolean isPrivate, boolean isUploaded){
		set_id(id);
		set_name(name);
		set_isBoycotted(isBoycotted);
		set_isDeleted(isDeleted);
		set_isPrivate(isPrivate);
		set_isUploaded(isUploaded);
	}
	
	public LocationNameVO(){
		
	}

	public long get_id() {
		return _id;
	}

	public void set_id(long _id) {
		this._id = _id;
	}

	public String get_name() {
		return _name;
	}

	public void set_name(String _name) {
		this._name = _name;
	}

	public boolean is_isBoycotted() {
		return _isBoycotted;
	}

	public void set_isBoycotted(boolean _isBoycotted) {
		this._isBoycotted = _isBoycotted;
	}

	public boolean is_isDeleted() {
		return _isDeleted;
	}

	public void set_isDeleted(boolean _isDeleted) {
		this._isDeleted = _isDeleted;
	}

	public boolean is_isPrivate() {
		return _isPrivate;
	}

	public void set_isPrivate(boolean _isPrivate) {
		this._isPrivate = _isPrivate;
	}

	public boolean is_isUploaded() {
		return _isUploaded;
	}

	public void set_isUploaded(boolean _isUploaded) {
		this._isUploaded = _isUploaded;
	}
}
